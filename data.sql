create database EmployeeSystem;
use EmployeeSystem;

create table E_information
(Eno char(20) primary key,
 Ename char(20),
 Eage int,
 Esex enum('男','女'),
 Ebirth date,
 Eaddress char(20),
 Ephoneno char(20),
 Sno char(20) references Section(Sno));

create table Section
(Sno char(10) primary key,
 Sname char(10),
 Majorname char(20));
 
create table E_attendance
(Eno char(20) primary key,
 Attendance_days int,
 Leave_days int,
 Absence_days int);
 
create table E_salary
(Eno char(20) primary key,
 E_basesalary int,
 E_bonus int,
 E_dsalary int,
 E_insurance int,
 E_tax int,
 E_fsalary int);
 
